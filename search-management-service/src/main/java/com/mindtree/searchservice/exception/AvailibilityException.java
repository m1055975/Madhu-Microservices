package com.mindtree.searchservice.exception;

public class AvailibilityException extends DaoException {

	
	private static final long serialVersionUID = 1L;

	public AvailibilityException() {
		super();
	
	}

	public AvailibilityException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public AvailibilityException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public AvailibilityException(String message) {
		super(message);
	
	}

	public AvailibilityException(Throwable cause) {
		super(cause);
	
	}
	
	

}
